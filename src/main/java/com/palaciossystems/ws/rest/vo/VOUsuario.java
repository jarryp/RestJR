package com.palaciossystems.ws.rest.vo;

public class VOUsuario {
	private String usuario;
	private String passwdor;
	private boolean userValido;
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getPasswdor() {
		return passwdor;
	}
	public void setPasswdor(String passwdor) {
		this.passwdor = passwdor;
	}
	public boolean isUserValido() {
		return userValido;
	}
	public void setUserValido(boolean userValido) {
		this.userValido = userValido;
	}
	
	

}
